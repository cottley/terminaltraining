    this.terminal.writeln('───────────────────────────────────────');
    this.terminal.writeln('# You can practice all preinstall tasks here:');
    this.terminal.writeln('groupadd oinstall');
    this.terminal.writeln('useradd -g oinstall -G dba oracle');
    this.terminal.writeln('mkdir -p /u01/app/oracle/product/19.0.0/dbhome_1');
    this.terminal.writeln('vim /etc/sysctl.conf');
    this.terminal.writeln('cat /etc/passwd | grep oracle');
    this.terminal.writeln('id oracle');
    this.terminal.writeln('');
