    this.terminal.writeln('   netca');
    this.terminal.writeln('   # Create default LISTENER on port 1521');
    this.terminal.writeln('');
    this.terminal.writeln('3. Enable Oracle at Startup:');
    this.terminal.writeln('   vim /etc/oratab');
    this.terminal.writeln('   # Change N to Y for your database:');
    this.terminal.writeln('   # ORCL:/u01/app/oracle/product/19.0.0/dbhome_1:Y');
    this.terminal.writeln('');
    this.terminal.writeln('█▀▀ TESTING & VERIFICATION');
    this.terminal.writeln('──────────────────────────');
    this.terminal.writeln('1. Check Listener Status:');
    this.terminal.writeln('   lsnrctl status');
    this.terminal.writeln('');
    this.terminal.writeln('2. Connect to Database:');
    this.terminal.writeln('   sqlplus / as sysdba');
    this.terminal.writeln('   SQL> SELECT name, open_mode FROM v$database;');
    this.terminal.writeln('   SQL> SELECT instance_name, status FROM v$instance;');
    this.terminal.writeln('');
    this.terminal.writeln('3. Check Alert Log:');
    this.terminal.writeln('   cd $ORACLE_BASE/diag/rdbms/orcl/ORCL/trace');
    this.terminal.writeln('   tail -f alert_ORCL.log');
    this.terminal.writeln('');
    this.terminal.writeln('4. Test Remote Connection:');
    this.terminal.writeln('   sqlplus system@localhost:1521/ORCL');
    this.terminal.writeln('');
    this.terminal.writeln('█▀▀ COMMON ISSUES & SOLUTIONS');
    this.terminal.writeln('─────────────────────────────');
